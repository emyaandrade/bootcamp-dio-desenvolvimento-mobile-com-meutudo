//Foi criado em 2009 pelo Ryan Dahl (um engenheiro de software, americano), lançado em um ambiente de execução de JavaScript do lado do servidor (ou seja, você não estava mais preso do lado dos navegadores), ao invés de rodar o JS no front-End, você poderia rodar o JS do lado do servidor (Back-End).
//O Node.js nasceu de uma necessidade muito específica: rodar do lado do servidor, sem precisar do navegador.

//em 2010 a versão 0.1.14 do Node.js foi lançada, marcando o desenvolvimento da comunidade e o crescente interesse na plataforma. Foi lançado totalmente de graça.

//em 2011 foi adotado por grandes empresas como Microsoft, Yahoo!, LinkedIn. As empresas começaram a se apaixonar pelo Node.js e as possibilidades do JavaScript no back-end além do front-end.
//atualmente encontramos node.js em várias empresas gigantes, como: netflix, nasa, wallmart, ebay, trello, etc

//em 2012 o fundador do node.js "abandonou" esse projeto, mas a comunidade e as empresas já estavam usando e gostando, então houve a criação da Fundação Node.js como uma organização sem fins lucrativos para gerenciar e evoluir o projeto. Mais tarde essa fundação ficou conhecida como OpenJS Foundation
//para entender porque o fundador "abandonou" esse projeto, assista: 10 coisas que lamento sobre o Node.js - Ryan Dahl no Youtube
//resumidamente, ele abandonou porque teve coisas que não conseguiu evoluir como queria

//A OpenJS Foundation continua focada em evoluir o Node.js

//em 2013 teve o lançamento da versão 0.10 do Node.js, com melhorias de desempenho e estabilidade

//em 2015 a versão 0.12 do Node.js introduziu o suporte oficial para ECMAScript 6 (ES6), trazendo recursos modernos ao JavaScript.

//em 2016 houve o lançamento da versão 6 do node.js, marcando a transição para um ciclo de lançamento de versões Long Term Support (LTS).
//a partir da versão 6 sempre tem duas versões: uma versão com tudo estável, tudo que foi testado. E outra versão para quem é mais curioso, quem quer implementar coisas ali. Ou seja, uma versão segura para desenvolver e uma versão para quem gosta de testar e implementar coisas.

//em 2020 Ryan Dahl criou a Deno, o maior concorrente do Node.js, dessa vez fazendo tudo do jeito como ele queria.

//a linguagem do Deno é a mesma do Node.js, você pode transitar entre as duas, ou até fazer projeto que usam Deno e Node.js

//Basicamente o que fazemos com o Node.js é programar pequenos pacotinhos de JavaScript, daí o logo com js envolto em um "pacotinho" verde.