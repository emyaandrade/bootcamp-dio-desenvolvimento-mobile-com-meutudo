// node --watch

//O que é o node--watch?
//o node fica "vigiando" o arquivo e toda vez que tem uma alteração no arquivo, ele executa de novo.
//como usar?
//no terminal digite:
// node --watch nome-da-pasta/nome-do-arquivo.js
//depois de usar esse comando, cada alteração no arquivo será exibida no terminal, sem que precise usar o comando node --watch novamente, e sem que precise ficar a todo momento usando o comando node nome-da-pasta/nome-do-arquivo.js

//como desligar o modo node --watch?
//basta apertar Ctrl+C e ele para de executar, é o comando de fechar o modo node --watch